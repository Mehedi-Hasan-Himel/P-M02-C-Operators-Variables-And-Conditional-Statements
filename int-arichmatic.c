// summation substraction multiplication division of integer. 
#include <stdio.h>
int main(){
    int a = 10;
    int b = 5;

    // summation of 2 integer
    int sum = a+b;  
    printf("Sum = %d\n",sum);

    // substraction of 2 integer
    int sub = a-b;
    printf("Sub = %d\n",sub);

    // multiplication of 2 integer
    int mul = a*b;
    printf("mul = %d\n",mul);

    // division of 2 integer
    int div = a/b;
    printf("div = %d\n",div);

    return 0;
}

